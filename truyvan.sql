I. Truy vấn về Nhân sự và Giáo viên (1–8)

1. Liệt kê tất cả nhân sự đang làm việc trong trường.

2. Tìm thông tin giáo viên theo mã nhân sự cụ thể.

3. Đếm số lượng giáo viên theo từng phòng ban.

4. Liệt kê danh sách nhân sự theo vị trí công việc (CanBoNhanVien + ViTriCV_MoTaCV).

5. Hiển thị danh sách giáo viên có thâm niên nghề từ 5 năm trở lên.

6. Liệt kê danh sách nhân sự vào làm trong năm 2024.

7. Tìm những nhân sự có cùng địa chỉ hoặc email chứa “gmail.com”.

8. Liệt kê thông tin nhân sự có nhiều hơn 1 số điện thoại.

II. Truy vấn về Lớp học và Năm học (9–14)

9. Liệt kê tất cả các lớp học trong năm học 2024–2025.

10. Tính tổng sĩ số học sinh của mỗi lớp.

11. Liệt kê giáo viên chủ nhiệm của từng lớp.

12. Tìm lớp học có sĩ số lớn hơn 30 học sinh.

13. Hiển thị danh sách các năm học và thời gian bắt đầu – kết thúc.

14. Tìm những năm học đã kết thúc (so sánh với ngày hiện tại).

III. Truy vấn về Học sinh (15–20)

15. Liệt kê danh sách học sinh theo lớp và năm học.

16. Tìm học sinh theo họ tên phụ huynh.

17. Tính tổng số học sinh đang theo học trong năm hiện tại.

18. Liệt kê những học sinh có trạng thái học tập là “Bảo lưu” hoặc “Nghỉ học”.

19. Tìm các học sinh sinh trong tháng 9 (để tổ chức sinh nhật).

20. Liệt kê danh sách học sinh cùng giới tính trong cùng một lớp.

IV. Truy vấn về Khoản phí và Đóng phí (21–25)

21. Liệt kê tất cả các khoản phí cần đóng trong năm học 2024–2025.

22. Tính tổng số tiền phải đóng của một học sinh trong một năm học.

23. Tính tổng số tiền đã thu được cho từng khoản phí trong năm học hiện tại.

24. Tìm danh sách học sinh còn nợ học phí (ConNo > 0).

25. Liệt kê học sinh đã hoàn tất toàn bộ các khoản đóng phí.

V. Truy vấn về Bán trú và Bữa ăn (26–28)

26. Liệt kê danh sách học sinh tham gia ăn bán trú trong ngày cụ thể.

27. Tính tổng số học sinh ăn bán trú theo từng ngày trong tháng.

28. Liệt kê các món ăn đã được phục vụ trong ngày 15/11/2025.

VI. Truy vấn về Cơ sở vật chất và Quản lý phòng (29–30)

29. Liệt kê danh sách các phòng học cùng sức chứa và người phụ trách quản lý.
  
30. Tính tổng giá trị cơ sở vật chất của từng phòng (tổng GiaTri theo TenPhong).